# Built-in Polymorphism Examples

# len() - string ke liye length of chars, list ke liye elements count
print(len("Noor"))       # Output: 4
print(len([1, 2, 3, 4])) # Output: 4

# + operator - int ke liye addition, string ke liye concatenation
print(10 + 20)           # Output: 30
print("Noor" + "deep")   # Output: "Noordeep"
